void CHtmlOpenResultDlg::OnOK() 
{
	//コントロール−＞メンバ変数データ取り込み
	UpdateData(TRUE);

	//ウインドウの状態と位置を記憶させる
	WINDOWPLACEMENT		wp;
	if(GetWindowPlacement(&wp))
	{
		if(IsZoomed())
			wp.flags |= WPF_RESTORETOMAXIMIZED;
		SaveWindowPlacement(&wp);
	}

	CDialog::OnOK();
}

